            <h3>A Sanctuary in the Florida Keys</h3>

            <p>Warning signs of the fragility and finite nature of marine resources in the Florida Keys have been present for years. Concerns over pollution, overfishing, overuse, misuse, and the subsequent demise of the area's coral reefs, eventually led to the establishment of the Key Largo and Looe Key National Marine Sanctuaries.</p>


            <p>While these sanctuaries and other marine protected areas, such as John Pennekamp Coral Reef State Park, were steps in the right direction to protect reefs, things in the Keys continued to decline. In 1990, amidst concerns about deteriorating water quality, the threat of oil drilling, and numerous vessel groundings, Congress designated the Florida Keys National Marine Sanctuary, incorporating the preexisting Key Largo and Looe Key sanctuaries.</p>

            <p>Today, the sanctuary protects 2,900 square nautical miles of waters surrounding the Florida Keys, extending from south of Miami westward to encompass the Dry Tortugas, excluding the Dry Tortugas National Park. The shoreward boundary of the sanctuary is the mean high-water mark, meaning that once you set foot in Keys waters, you have entered the sanctuary.</p>

            <p>Within the boundaries of the sanctuary lie spectacular, unique, and nationally significant marine environments. The sanctuary protects the world's third largest barrier reef, which is composed of more than 6,000 individual reefs. More than 2,000 square nautical miles of seagrass meadows blanket the seafloor of South Florida. Approximately 1,700 mangrove-fringed islands account for over 1,800 miles of shoreline within the sanctuary. More than 6,000 species of marine life — including more than 500 species of fish and 50 species of coral — call these habitats home. The sanctuary also protects pieces of our nation's history such as shipwrecks and other archeological treasures.</p>
