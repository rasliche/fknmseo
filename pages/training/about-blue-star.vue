            <h3>Why Blue Star?</h3>

            <p>Florida Keys coral reefs are a main draw for your customers. But what if those corals were damaged, sick, or just went away? What would that mean for you, your customers, and your business?</p>

            <p>The reefs of the Florida Keys are stressed. And when reef users are uninformed or irresponsible, they can, and often do, make these stresses even worse. That's bad news.</p>

            <p>The good news is that, as a Blue Star partner, you have the opportunity to teach visitors about the reef and how to safely interact with it. If the information that you provide could change even one of those visitors' behaviors to be reef-aware, think about the good you could be doing.</p>

            <p>The Blue Star program is one way that you can show customers - people who were drawn to the area because of the environment in the first place - that you and the business that you work for care about the ecosystem and are committed to doing your part to protect it.</p>
