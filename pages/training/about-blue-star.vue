            <h3>What is Required as Part of Blue Star?</h3>

            <p>Blue Star operators have made a commitment to train their staff and customers to be better environmental stewards. As part of the program, operators are required to complete training each year on issues affecting coral reefs, Sanctuary regulations, and coral reef etiquette. In turn, these operators must educate their customers on these topics. They also must offer conservation-related dive courses and participate in activities such as fish counts and reef clean-ups.</p>

            <p>Once a business has gone through the initial evaluation and is recognized as a Blue Star partner, they will be reevaluated each year, to make sure that the Blue Star criteria are still being followed.</p>

