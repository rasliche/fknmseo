            <h3>What is Blue Star</h3>
            <p>The Blue Star program was established to recognize tour operators who work to reduce the impact of divers and snorkelers on coral reefs of the Florida Keys. Unlike some of the major causes of reef decline such as climate change, damage to reefs from diving and snorkeling is considered preventable.</p>

